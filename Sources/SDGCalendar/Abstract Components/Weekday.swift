/// A calendar compenent representing a day of the week.
public protocol Weekday : ConsistentlyOrderedCalendarComponent {

}

extension Weekday {

    /// Returns the English name.
    @_inlineable public func inEnglish() -> StrictString {
        switch ordinal {
        case 1:
            return "Sunday"
        case 2:
            return "Monday"
        case 3:
            return "Tuesday"
        case 4:
            return "Wednesday"
        case 5:
            return "Thursday"
        case 6:
            return "Friday"
        case 7:
            return "Saturday"
        default:
            unreachable()
        }
    }
}
